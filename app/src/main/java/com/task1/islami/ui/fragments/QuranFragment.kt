        suraAdapter = SuraAdapter(suraList)

        suraAdapter.onSuraClickListener = object : SuraAdapter.OnItemClickListener {
            override fun onItemClick(position: Int, suraName: String) {

                var intent: Intent = Intent(context, SuraDetailsActivity::class.java)

                intent.putExtra(Constants.SURA_NAME, suraName)
                intent.putExtra(Constants.SURA_POSITION, position)
                startActivity(intent)

            }

        }

        suraRecyclerView.adapter = suraAdapter
